#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*a;
	char	*b;

	a = (char *)src;
	b = (char *)dst;
	while (n-- > 0)
	{
		*b++ = *a++;
	}
	return (dst);
}
